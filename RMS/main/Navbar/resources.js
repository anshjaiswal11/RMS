document.addEventListener('DOMContentLoaded', function() {
    const subjectSelect = document.getElementById('subjectSelect');
    const videoContainer = document.getElementById('videoContainer');
    const resultMessage = document.getElementById('resultMessage');
    const clearAllButton = document.getElementById('clearAll');
    
    subjectSelect.addEventListener('change', function() {
        const subject = subjectSelect.value;
        displayVideos(subject);
    });
    
    clearAllButton.addEventListener('click', function() {
        subjectSelect.value = '';
        videoContainer.innerHTML = '';
        resultMessage.textContent = '0 Results Found';
    });
    
    function displayVideos(subject) {
        videoContainer.innerHTML = '';
        
        if (subject && videoData[subject]) {
            videoData[subject].forEach(video => {
                const videoCard = document.createElement('div');
                videoCard.classList.add('video-card');
                
                videoCard.innerHTML = `
                <iframe src="${video.url}" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>
                <div class="video-header">${video.title}</div>
            `;
                videoCard.querySelector('.video-header').style.color = 'black';
                videoContainer.appendChild(videoCard);
            });
            
            resultMessage.textContent = `${videoData[subject].length} Results Found`;
        } else {
            resultMessage.textContent = '0 Results Found';
        }
    }
    const hamburger = document.querySelector('.hamburger');
const navLinks = document.querySelector('.navbar-right');
const navlist_display = document.querySelector('.navbar-left');

hamburger.addEventListener('click', () => {
    navLinks.classList.toggle('active');
    navlist_display.classList.toggle('hidden');
});
});
